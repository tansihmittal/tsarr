// Helper to generate Google Fonts URL for loading
export const getGoogleFontsUrl = (fonts: string[]): string => {
  const googleFonts = fonts.filter(f => 
    !["Arial", "Helvetica", "Times New Roman", "Georgia", "Verdana", "Courier New", 
      "Impact", "Comic Sans MS", "Trebuchet MS", "Arial Black", "Palatino Linotype", 
      "Lucida Console", "Tahoma", "Century Gothic"].includes(f)
  );
  
  if (googleFonts.length === 0) return "";
  
  const fontParams = googleFonts
    .map(f => f.replace(/ /g, "+") + ":wght@100;200;300;400;500;600;700;800;900")
    .join("&family=");
  
  return `https://fonts.googleapis.com/css2?family=${fontParams}&display=swap`;
};

// Load a single font dynamically
export const loadFont = async (fontName: string): Promise<void> => {
  // Skip system fonts
  const systemFonts = ["Arial", "Helvetica", "Times New Roman", "Georgia", "Verdana", 
    "Courier New", "Impact", "Comic Sans MS", "Trebuchet MS", "Arial Black", 
    "Palatino Linotype", "Lucida Console", "Tahoma", "Century Gothic"];
  
  if (systemFonts.includes(fontName)) return;
  
  // Check if already loaded
  const existingLink = document.querySelector(`link[data-font="${fontName}"]`);
  if (existingLink) return;
  
  // Create and append link element
  const link = document.createElement("link");
  link.rel = "stylesheet";
  link.href = `https://fonts.googleapis.com/css2?family=${fontName.replace(/ /g, "+")}:wght@100;200;300;400;500;600;700;800;900&display=swap`;
  link.setAttribute("data-font", fontName);
  document.head.appendChild(link);
  
  // Wait for font to load
  return new Promise((resolve) => {
    link.onload = () => resolve();
    link.onerror = () => resolve(); // Resolve anyway to not block
  });
};
